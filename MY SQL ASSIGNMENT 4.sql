-- CREATE DATABASE 
CREATE DATABASE ASSIGNMENT4;
USE ASSIGNMENT4;

-- CREATING TABLES

CREATE TABLE COUNTRY
(
COUNTRY_ID INT AUTO_INCREMENT PRIMARY KEY,
COUNTRY_NAME VARCHAR(100),
POPULATION BIGINT,
AREA DECIMAL (10,2));

CREATE TABLE PERSONS
(
ID INT AUTO_INCREMENT PRIMARY KEY,
FNAME VARCHAR (50) NOT NULL,
LNAME VARCHAR(50),
POPULATION BIGINT,
RATING DECIMAL (3,2),
COUNTRY_ID INT,
COUNTRY_NAME VARCHAR(100));

DESC COUNTRY;
DESC PERSONS;

